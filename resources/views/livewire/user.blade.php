        <div><strong>Браузеры:</strong> Google Chrome, Mozilla Firefox, Opera, Yandex, Safari, Edge (последние версии)</div>
        <div><strong>Устройства:</strong> Персональный компьютер, ноутбук, смартфон, планшет, телевизор (SmartTV)</div>
        <div><strong>Интернет:</strong> Рекомендуется скорость от 5 Мбит/с</div>
