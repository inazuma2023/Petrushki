        <h2>1. Введение</h2>
        
        <h3>1.1. Назначение документа</h3>
        <p>Это руководство поможет вам быстро освоить функционал интернет-магазина <strong>Petrushki</strong>: от регистрации до оформления заказа и отслеживания доставки.</p>
        
        <h3>1.2. Об интернет-магазине</h3>
        <p><strong>Petrushki</strong> – это онлайн-платформа для покупки товаров содержания и ухода за домашнимим питомцами. У нас вы найдете корма, игрушки, средства ухода, амуницию, оборудование и лекарственные препораты для всех видов питомцев  и другие товары с доставкой по всей России.</p>
        
